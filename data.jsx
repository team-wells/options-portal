export function getDiffParamaeter(current, gridCallData, gridPutData) {
  var optionarray = [];

  var webparamobjcall = {
    spotPrice: current.state.spotPriceTxt,
    interestRate: current.state.interstRateTxt,
    requests: []
  };

  //CALL Grid
  for (var i=0;i<3;i++) {
    var arrayobj = {
      strikePrice: "",
      expireDate: "",
      optionType: "",
      impliedVolatility: ""
    };

    arrayobj.strikePrice = gridCallData[i].StrikePrice;
    arrayobj.expireDate = current.state.selectExpDateChange;
    arrayobj.optionType = "CALL";
    arrayobj.impliedVolatility = gridCallData[i].CallVolatility;

    optionarray.push(arrayobj);
  }
  
  //PUT Grid
  for (var j=0;j<3;j++) {
    var arrayobj1 = {
      strikePrice: "",
      expireDate: "",
      optionType: "",
      impliedVolatility: ""
    };

    arrayobj1.strikePrice = gridPutData[j].StrikePrice;
    arrayobj1.expireDate = current.state.selectExpDateChange;
    arrayobj1.optionType = "PUT";
    arrayobj1.impliedVolatility = gridPutData[j].PutVolatility;

    optionarray.push(arrayobj1);
  }
  webparamobjcall.requests = optionarray;

  return webparamobjcall;
}

export function getParamaeter(current, gridCallData, gridPutData) {
  var optionarray = [];

  var webparamobjcall = {
    model: current.state.selectModelOption,
    spotPrice: current.state.spotPriceTxt,
    interestRate: current.state.interstRateTxt,
    requests: []
  };

  //CALL Grid
  for (var key in gridCallData) {
    var arrayobj = {
      strikePrice: "",
      expireDate: "",
      optionType: "",
      impliedVolatility: ""
    };

    arrayobj.strikePrice = gridCallData[key].StrikePrice;
    arrayobj.expireDate = current.state.selectExpDateChange;
    arrayobj.optionType = "CALL";
    arrayobj.impliedVolatility = gridCallData[key].CallVolatility;

    optionarray.push(arrayobj);
  }

  //PUT Grid
  for (var key1 in gridPutData) {
    var arrayobj1 = {
      strikePrice: "",
      expireDate: "",
      optionType: "",
      impliedVolatility: ""
    };

    arrayobj1.strikePrice = gridPutData[key1].StrikePrice;
    arrayobj1.expireDate = current.state.selectExpDateChange;
    arrayobj1.optionType = "PUT";
    arrayobj1.impliedVolatility = gridPutData[key1].PutVolatility;

    optionarray.push(arrayobj1);
  }
  
  webparamobjcall.requests = optionarray;

  return webparamobjcall;
}
